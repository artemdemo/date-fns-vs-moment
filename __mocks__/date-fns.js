export const format = (date, formatStr) => `${date} [${formatStr}]`;

export const differenceInDays = (from, to) => `${from} -> ${to}`;

export const distanceInWords = (from, to) => `${from} -> ${to}`;

export const addMinutes = (date, offset) => `${date} + ${offset}`;

export const subMinutes = (date, offset) => `${date} - ${offset}`;